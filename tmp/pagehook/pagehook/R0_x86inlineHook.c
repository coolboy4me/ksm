typedef void(__stdcall *GETCODELENGTH)(PVOID code, DWORD *len);

BOOLEAN inlineHook(HOOK_INFO *lpHookInfo)
{
	UCHAR*			shellcode;
	ULONG_PTR		count = 0;
	ULONG_PTR		copyCodeSize = 0;
	ULONG_PTR		jmpOrigPointer = 0;
	UINT			decodedInstructionsCount = 0;
	ULONG lenone = 0;
	ULONG len = -1;
	PVOID pszShellCode = NULL;
	BYTE CodeJmp[] = { 0x68, 0x00, 0x00, 0x00, 0x00, 0xC3 };
	GETCODELENGTH GetCodeLength;

	GetCodeLength = (GETCODELENGTH)dis;
	if (MmIsAddressValid(lpHookInfo) == FALSE ||\
		MmIsAddressValid((PVOID)lpHookInfo->fnOrigAddress) == FALSE ||\
		MmIsAddressValid((PVOID)lpHookInfo->fnNewAddress) == FALSE ||
		lpHookInfo->isHook == TRUE)
	{
		return FALSE;
	}
	
	shellcode = (UCHAR*)ExAllocatePool(NonPagedPool,MAX_SHELLCODE_LEN);
	memset(shellcode,0x90,MAX_SHELLCODE_LEN);
	//******************************************************************************
	while (copyCodeSize < MAX_INSTRUCTIONS)
	{
		GetCodeLength(lpHookInfo->fnOrigAddress + copyCodeSize, &len);
		copyCodeSize += len;
		if (copyCodeSize >= 5)
			break;
	}
	//******************************************************************************
	jmpOrigPointer = lpHookInfo->fnOrigAddress + copyCodeSize;
	memcpy(lpHookInfo->origCode, (PVOID)lpHookInfo->fnOrigAddress, copyCodeSize);
	memcpy(shellcode, (PVOID)lpHookInfo->fnOrigAddress, copyCodeSize);
	memcpy(shellcode + copyCodeSize, CodeJmp, 6);
	memcpy(shellcode + copyCodeSize + 1, &jmpOrigPointer, sizeof(PVOID));
	mem_protect_close();
	memset((PVOID)lpHookInfo->fnOrigAddress,0x90,copyCodeSize);
	//******************************************************************************
	*(PBYTE)(lpHookInfo->fnOrigAddress + 0x0) = 0xE9;
	*(PULONG)(lpHookInfo->fnOrigAddress + 0x1) = (ULONG)lpHookInfo->fnNewAddress - (ULONG)lpHookInfo->fnOrigAddress - 5;
	//******************************************************************************
	mem_protect_open();

	lpHookInfo->isHook = TRUE;
	lpHookInfo->copyCodeSize = copyCodeSize;
	lpHookInfo->fnNormalExec = (ULONG)shellcode;
	
	return TRUE;
}


void unInlineHook(HOOK_INFO *lpHookInfo)
{
	if (MmIsAddressValid(lpHookInfo) == FALSE ||\
		MmIsAddressValid((PVOID)lpHookInfo->fnOrigAddress) == FALSE ||\
		MmIsAddressValid((PVOID)lpHookInfo->fnNewAddress) == FALSE ||\
		MmIsAddressValid((PVOID)lpHookInfo->fnNormalExec) == FALSE ||
		lpHookInfo->isHook == FALSE)
	{
		return;
	}

	mem_protect_close();
	memcpy((PVOID)lpHookInfo->fnOrigAddress, lpHookInfo->origCode, lpHookInfo->copyCodeSize);
	mem_protect_open();

	ExFreePool((PVOID)lpHookInfo->fnNormalExec);
	lpHookInfo->isHook = FALSE;
}